// Helper function to get translation
export const t = (language, key) => {
  const keys = key.split('.');
  let value = translations[language];
  
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      console.warn(`Translation missing for key: ${key} in language: ${language}`);
      return key;
    }
  }
  
  return value;
};
